class FakeItemSearchService:
    """In-memory search service for librarian operation contract tests."""

    def __init__(self) -> None:
        """Initialize captured search state."""
        self.searched_item_type: ItemType | None = None

    async def search(
        self,
        *,
        query: str,
        item_type: ItemType | None = None,
    ) -> list[dict[str, JSONValue]]:
        """Return one skill recommendation result."""
        self.searched_item_type = item_type
        return [
            {
                "id": "00000000-0000-4000-8000-000000000321",
                "item_type": "SKILL",
                "title": f"Result for {query}",
                "summary": "Recommended skill.",
                "content": "Use public JSON enum values at strict boundaries.",
                "category_id": None,
                "tags": ["strict-json"],
                "status": "ACTIVE",
                "source_type": "USER_CREATED",
                "created_by_type": "USER",
                "created_by_name": "alex",
                "details": {},
                "created_at": datetime(2026, 5, 12, 10, 0, tzinfo=UTC),
                "updated_at": datetime(2026, 5, 12, 10, 5, tzinfo=UTC),
            }
        ]


class FakeLibrarianGenerationService:
    """Deterministic librarian candidate service for router contract tests."""

    def generate_candidate_stub(
        self,
        *,
        provider_id: str,
        prompt: str,
    ) -> CreateSkillCandidateResult:
        """Return a deterministic generated skill candidate."""
        return CreateSkillCandidateResult(
            title="Generated strict JSON skill",
            summary="Candidate generated by librarian.",
            content=f"Provider {provider_id} generated: {prompt}",
            purpose=prompt,
            input_schema={"type": "object"},
            output_schema={"type": "object"},
            required_tools=["pytest"],
            risk_level=RiskLevel.LOW,
            version="1.0.0",
            prompt=prompt,
            provider_id=provider_id,
        )


def test_recommend_accepts_json_item_type_string_and_returns_items() -> None:
    """POST /librarians/recommend should accept public item_type enum strings."""
    item_service = FakeItemSearchService()

    def override_item_service() -> FakeItemSearchService:
        return item_service

    with override_library_provider("item_service", override_item_service()):
        with TestClient(app, raise_server_exceptions=False) as client:
            response = client.post(
                "/librarians/recommend",
                json={"query": "strict JSON", "item_type": "SKILL", "limit": 1},
            )

    assert response.status_code == 200
    assert response.json() == [
        {
            "id": "00000000-0000-4000-8000-000000000321",
            "item_type": "SKILL",
            "title": "Result for strict JSON",
            "summary": "Recommended skill.",
            "content": "Use public JSON enum values at strict boundaries.",
            "category_id": None,
            "tags": ["strict-json"],
            "status": "ACTIVE",
            "source_type": "USER_CREATED",
            "created_by_type": "USER",
            "created_by_name": "alex",
            "details": {},
            "created_at": "2026-05-12T10:00:00Z",
            "updated_at": "2026-05-12T10:05:00Z",
        }
    ]
    assert item_service.searched_item_type is ItemType.SKILL


def test_create_skill_candidate_returns_strict_item_response_shape() -> None:
    """POST /librarians/create-skill-candidate should return valid ItemResponse JSON."""

    def override_librarian_service() -> FakeLibrarianGenerationService:
        return FakeLibrarianGenerationService()

    with override_library_provider("librarian_service", override_librarian_service()):
        with TestClient(app, raise_server_exceptions=False) as client:
            response = client.post(
                "/librarians/create-skill-candidate",
                json={
                    "provider_id": "00000000-0000-4000-8000-000000000501",
                    "prompt": "Create a strict JSON skill.",
                    "category_id": "00000000-0000-4000-8000-000000000002",
                },
            )

    assert response.status_code == 200
    body = response.json()
    assert body == {
        "id": "draft-skill-candidate",
        "item_type": "SKILL",
        "title": "Generated strict JSON skill",
        "summary": "Candidate generated by librarian.",
        "content": "Provider 00000000-0000-4000-8000-000000000501 generated: Create a strict JSON skill.",
        "category_id": "00000000-0000-4000-8000-000000000002",
        "tags": ["draft", "librarian"],
        "status": "DRAFT",
        "source_type": "LIBRARIAN_CREATED",
        "created_by_type": "LIBRARIAN",
        "created_by_name": "librarian",
        "details": {
            "purpose": "Create a strict JSON skill.",
            "input_schema": {"type": "object"},
            "output_schema": {"type": "object"},
            "required_tools": ["pytest"],
            "risk_level": "LOW",
            "version": "1.0.0",
        },
        "created_at": body["created_at"],
        "updated_at": body["updated_at"],
    }
    assert isinstance(body["created_at"], str)
    assert body["created_at"] == body["updated_at"]
